Map::Map() {
	background = TextureManager::loadTexture("assets/background.png");
	floor = TextureManager::loadTexture("assets/floor.png");

	loadMap(lvl1);

	srcRect.x = srcRect.y = 0;
	srcRect.w = destRect.w = 32;
	srcRect.h = destRect.h = 32;

	destRect.x = destRect.y = 0;
}

void Map::loadMap(int arr[20][25]) {
	for (int i = 0; i < 20; i++) {
		for (int j = 0; j < 25; j++)
			map[i][j] = arr[i][j];
	}
}

void Map::drawMap() {
	int type = 0;
	for (int i = 0; i < 20; i++) {
		for (int j = 0; j < 25; j++) {
			type = map[i][j];

			destRect.x = j * 32;
			destRect.y = i * 32;

			switch (type) {
			case 0:
				TextureManager::draw(background, srcRect, destRect);
				break;
			case 1:
				TextureManager::draw(floor, srcRect, destRect);
				break;
			default:
				break;
			}
		}
	}
}